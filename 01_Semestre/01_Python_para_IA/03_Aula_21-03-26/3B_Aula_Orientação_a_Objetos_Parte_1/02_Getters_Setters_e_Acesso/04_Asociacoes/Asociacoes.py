#  relação entre as classes Disciplina e Pessoa é uma associação.
# Algumas propriedades de uma associação:
# ● O objeto associado (Pessoa) não tem relação direta com a classe (Disciplina).
# ● O objeto associado (Pessoa) pode pertencer a mais de uma relação ao mesmo tempo.
# ● A classe (Disciplina) não gerencia a existência do objeto associado.
# ● A pessoa que representa o professor da disciplina existe independentemente da classe e objetos Disciplina.
# ● O dado membro associado (Pessoa) pode ou não saber da existência da classe (Disciplina).