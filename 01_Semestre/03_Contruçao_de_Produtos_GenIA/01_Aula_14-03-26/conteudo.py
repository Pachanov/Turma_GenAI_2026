##########Organizacao de codigo##########

#-Organizacao de codigo é crucial para a manutenibilidade e colaboracao em projetos de GenIA.

#Boas praticas:
#Estrutura de pastas: Organize o codigo em pastas logicas (ex: data, models, utils).
#Readme.md: Fornece uma visao geral do projeto, instrucoes de instalacao e uso.
#Nomenclatura sistematica e desciptiva: nomear arquivos e script de forma ordenada e clara.

##########Documentacao e manutençao de codigo##########

#Documentaçao do codigo: Inclua comentarios claros e detalhados para explicar a logica e o funcionamento do codigo.
#controle de versao: Use sistemas de controle de versao (ex: Git) para acompanhar as mudancas no codigo e facilitar a colaboracao.
#Testes automatizados: Implemente testes automatizados para garantir a qualidade e confiabilidade
#ambiente virtual isolado: Use ambientes virtuais (ex: virtualenv, conda) para gerenciar dependencias e evitar conflitos entre projetos.
#scrips principais e scripts auxiliares: Separe scripts principais (ex: treinamento, avaliacao) de scripts auxiliares (ex: preprocessamento, visualizacao) para melhorar a organizacao e facilitar a manutencao.


##########Reprudutibilidade##########

#roda na minha maquina nao e reprodutibilidade
#ambientes computaçionais variam entre usuarios e sistemas
#versoes diferentes de pacotes e dependencias podem levar a resultados diferentes
#reprodutibilidade é fundamental para:
#1)transparencia cientifica: Permite que outros pesquisadores verifiquem e validem os resultados.
#2)verificaçao de resultados: Facilita a detecçao de erros e a melhoria do modelo.
#3)colaboraçao: Facilita a colaboraçao entre pesquisadores e equipes, permitindo
#4)ciclo de vida do projeto: Facilita a manutencao e atualizaçao do projeto ao longo do tempo.


#Git:PRINCIPAIS COMANDOS
#git init: Inicializa um novo repositório Git.
#git add <arquivo>: Adiciona um arquivo específico ao staging area.
#git commit -m "mensagem": Salva as mudanças no repositório com uma mensagem descritiva.
#git push: Envia as mudanças para um repositório remoto (ex: GitHub
#git pull: Atualiza o repositório local com as mudanças do repositório remoto.
#git clone <url>: Clona um repositório remoto para o local.
#git status: Mostra o status dos arquivos (modificados, não rastreados, etc.).
#git log: Exibe o histórico de commits.
#git diff: Mostra as diferenças entre arquivos modificados e a última versão commitada.
#git branch: Lista as branches existentes e indica a branch atual.
#git checkout <branch>: Muda para a branch especificada.
#git merge <branch>: Mescla a branch especificada com a branch atual.
#git pull request: Solicita a revisão e mesclagem de mudanças em um repositório remoto (usado em plataformas como GitHub).
